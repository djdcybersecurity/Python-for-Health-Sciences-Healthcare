# Chapter 4: Basic Operations in Python
# Demonstrating arithmetic, comparison, and logical operations

a, b = 10, 3

# Arithmetic Operations
sum_value = a + b
multiplication = a * b
modulo = a % b

# Logical and Comparison Operations
is_greater = a > b
both_true = (a > 5) and (b < 5)

# Print Results
print(f"Sum: {sum_value}, Multiplication: {multiplication}, Modulo: {modulo}")
print(f"Is A greater than B? {is_greater}, Are both conditions true? {both_true}")
