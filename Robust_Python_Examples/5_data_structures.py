# Chapter 5: Data Structures in Python
# Lists, Tuples, Sets, and Dictionary operations

# Lists
patient_records = ["John Doe", "Jane Smith", "Alex Brown"]
patient_records.append("Lisa White")

# Tuples
medical_tests = ("Blood Test", "MRI", "CT Scan")

# Sets
unique_patient_ids = {101, 102, 103, 104, 101}  # Duplicates are removed

# Dictionaries
patient_info = {"Name": "John Doe", "Age": 30, "Condition": "Diabetes"}

# Print Data Structures
print(f"Patients: {patient_records}, Tests: {medical_tests}")
print(f"Unique IDs: {unique_patient_ids}, Patient Info: {patient_info}")
